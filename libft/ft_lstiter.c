#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list	*tmp;

	tmp = lst;
	while (lst->next != NULL)
	{
		lst = tmp;
		f(lst);
		tmp = lst->next;
	}
}
