#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	unsigned int i;

	i = (n < 0) ? -n : n;
	(n < 0) ? ft_putchar_fd('-', fd) : i;
	if (i > 9)
		ft_putnbr_fd(i / 10, fd);
	ft_putchar_fd(i % 10 + '0', fd);
}
